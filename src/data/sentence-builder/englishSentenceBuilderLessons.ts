import type { Level, SentenceBuilderLesson, SentenceStage } from "../../types";

const skoolReturnUrl = "https://www.skool.com/agedown-transform-after-50-7367/classroom/8382e0b9?md=overview";

function guide(word: string, translation: string, source: string, target: string, note?: string) {
  return {
    word,
    translation,
    exampleSentences: [{ source, translation: target }],
    note,
  };
}

function breakdown(items: Array<[string, string, string?]>): NonNullable<SentenceStage["wordBreakdown"]> {
  return items.map(([source, target, note]) => ({ source, target, note }));
}

function stage(
  id: string,
  title: string,
  newVocab: string[],
  fullVocab: string[],
  prompt: string,
  targetAnswer: string,
  explanation: string,
  wordBreakdown: NonNullable<SentenceStage["wordBreakdown"]>,
  hints: string[] = [],
  acceptedAnswers: string[] = [],
  vocabGuide: NonNullable<SentenceStage["vocabGuide"]> = [],
): SentenceStage {
  return {
    id,
    title,
    newVocab,
    fullVocab,
    prompt,
    targetAnswer,
    acceptedAnswers,
    hints,
    explanation,
    wordBreakdown,
    vocabGuide,
  };
}

function lesson(
  id: string,
  title: string,
  subtitle: string,
  level: Level,
  tags: string[],
  finalChallenge: string,
  stages: SentenceStage[],
): SentenceBuilderLesson {
  return {
    id,
    title,
    subtitle,
    languageTarget: "english",
    learnerNativeLanguage: "spanish",
    level,
    tags: ["english", "sentence-builder", ...tags],
    estimatedMinutes: 14,
    skoolReturnUrl,
    relatedCourse: "Constructor de Frases en Inglés: de A1 a C2",
    activityType: "sentence-builder",
    data: {
      finalChallenge,
      stages,
    },
  };
}

