const americanHighlights = Object.fromEntries(americanVocab.map((item) => [item.term, { phrase: item.term, meaning: item.meaning }]));

function cardFromVocab(item: VocabItem): FlashcardItem {
  return {
    id: item.id,
    term: item.term,
    definition: item.meaning,
    exampleSentence: item.example,
    exampleTranslation: item.translation,
    acceptedAnswers: [item.meaning.split("/")[0].trim()],
    languageFrom: "english",
    languageTo: "spanish",
    difficulty: item.starred ? "hard" : "medium",
    starred: item.starred,
  };
}

function highlights(phrases: string[]) {
  return phrases.map((phrase) => americanHighlights[phrase]).filter((item): item is { phrase: string; meaning: string } => Boolean(item));
}

function message(
  id: string,
  speakerId: string,
  text: string,
  translation: string,
  phrases: string[],
  messageType: StoryMessage["messageType"] = "text",
  audioUrl?: string,
): StoryMessage {
  return {
    id,
    speakerId,
    messageType,
    text,
    translation,
    ...(audioUrl ? { audioUrl } : {}),
    vocabHighlights: highlights(phrases),
  };
}

