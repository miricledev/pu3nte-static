import type { FlashcardDeck, FlashcardItem, Level } from "../../types";

type CardSeed = {
  term: string;
  definition: string;
  example: string;
  note: string;
  difficulty?: "easy" | "medium" | "hard";
  starred?: boolean;
};

type DeckSeed = {
  storyId: string;
  title: string;
  subtitle: string;
  level: Level;
  cards: CardSeed[];
};

function slug(text: string, index: number) {
  return `${index + 1}-${text.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "").slice(0, 42)}`;
}

function makeCards(cards: CardSeed[]): FlashcardItem[] {
  return cards.map((card, index) => ({
    id: slug(card.term, index),
    term: card.term,
    definition: card.definition,
    exampleSentence: card.example,
    exampleTranslation: card.definition,
    notes: card.note,
    difficulty: card.difficulty ?? "easy",
    starred: card.starred,
    languageFrom: "english",
    languageTo: "spanish",
  }));
}

function makeDeck(seed: DeckSeed): FlashcardDeck {
  return {
    id: `${seed.storyId}-flashcards`,
    title: seed.title,
    subtitle: seed.subtitle,
    languageTarget: "english",
    learnerNativeLanguage: "spanish",
    level: seed.level,
    tags: ["Historias por Chat en Inglés — A1 a C2", "story vocab", seed.storyId],
    estimatedMinutes: seed.cards.length >= 40 ? 10 : 8,
    skoolSectionName: "Historias por Chat en Inglés — A1 a C2",
    relatedCourse: seed.storyId,
    activityType: "flashcards",
    data: { cards: makeCards(seed.cards) },
  };
}

